export function inferSiteCategory(article) {
  if (article.siteCategory) {
    const s = article.siteCategory;
    return LEGACY_SITE_CATEGORY[s] ?? s;
  }
  const bySlug = SITE_CATEGORY_BY_SLUG[article.slug];
  if (bySlug) return bySlug;
  if (article.clubSlug) return "main-news";
  switch (article.category) {
    case "botola":
      return "botola-news";
    case "national-team":
      return "national-team";
    case "caf":
      return "botola-news";
    case "morocco-sports":
      return "morocco-news";
    case "club":
      return "main-news";
    case "arab-world":
      return "professional-players";
    case "variety":
      return "voice-and-image";
    case "trending":
      if (article.slug === "trending-sports-governance") return "main-news";
      if (article.slug === "trending-var-discussion") return "flash-sports";
      return "flash-sports";
    default:
      return "all-news";
  }
}

export function getArticlesBySiteCategory(slug) {
  return ARTICLES.filter((a) => inferSiteCategory(a) === slug);
}

export function getArticlesByClub(clubSlug) {
  return ARTICLES.filter(
    (a) =>
      a.clubSlug === clubSlug ||
      (Array.isArray(a.linkedClubs) && a.linkedClubs.includes(clubSlug))
  );
}

export function getArticlesByCategory(category) {
  return ARTICLES.filter((a) => a.category === category);
}

export function getArticleBySlug(slug) {
  return ARTICLES.find((a) => a.slug === slug) ?? null;
}

export function getAllSlugs() {
  return ARTICLES.map((a) => a.slug);
}

